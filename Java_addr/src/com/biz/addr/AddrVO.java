package com.biz.addr;

public class AddrVO {
	
	 private String name;
	 private String tel;
	 private String addr;
	 private int age;
	
	 public String getName() {
		
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getTel() {
		return tel;
	}
	public void setTel(String tel) {
		this.tel = tel;
	}
	public String getAddr() {
		return addr;
	}
	public void setAddr(String addr) {
		this.addr = addr;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	 
	 
	
	
	
	
	
	
	
}